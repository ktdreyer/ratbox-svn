/*
 * Note: At least at protocol level ERROR has only one parameter,
 * although this is called internally from other functions
 * --msa
 *
 *      parv[0] = sender prefix
 *      parv[*] = parameters
 */
void m_error(struct Client *client_p, struct Client *source_p,
             int parc, char *parv[])
{
  char* para;

  para = (parc > 1 && *parv[1] != '\0') ? parv[1] : "<>";
  
  deprintf("error", "Received ERROR message from %s: %s",
	   source_p->name, para);

  /*
   * Ignore error messages generated by normal user clients
   * (because ill-behaving user clients would flood opers
   * screen otherwise). Pass ERROR's from other sources to
   * the local operator...
   */
  if (IsPerson(client_p) || IsUnknown(client_p))
    return;
  if (client_p == source_p)
    {
      sendto_realops_flags(FLAGS_ADMIN,
            "ERROR :from %s -- %s",
	    get_client_name(client_p, HIDE_IP), para);
      sendto_realops_flags(FLAGS_ALL,"ERROR :from %s -- %s",
			 get_client_name(client_p, MASK_IP), para);
    }
  else
    {
      sendto_realops_flags(FLAGS_NOTADMIN,
            "ERROR :from %s via %s -- %s",
	    source_p->name, get_client_name(client_p, MASK_IP), para);
      sendto_realops_flags(FLAGS_ADMIN,"ERROR :from %s via %s -- %s", source_p->name,
			 get_client_name(client_p, HIDE_IP), para);
    }
}
void ms_error(struct Client *client_p, struct Client *source_p,
              int parc, char *parv[])
{
  char* para;

  para = (parc > 1 && *parv[1] != '\0') ? parv[1] : "<>";
  
  deprintf("error", "Received ERROR message from %s: %s",
	   source_p->name, para);

  if (client_p == source_p)
    sendto_realops_flags(FLAGS_ALL,"ERROR :from %s -- %s",
			 get_client_name(client_p, MASK_IP), para);
  else
    sendto_realops_flags(FLAGS_ALL,"ERROR :from %s via %s -- %s", source_p->name,
			 get_client_name(client_p, MASK_IP), para);
}
